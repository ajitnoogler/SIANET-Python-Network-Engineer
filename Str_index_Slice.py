a = "Welcome"

print(a[0:7]) # Slice from index 0 up to (but not including) index 7
print(a[1:6]) # Slice from index 1 to index 5
print(a[:-2]) # means slice from start up to (but not including) the 2nd-last character
print(a[-2:]) # means slice from 2nd-last character to the end


